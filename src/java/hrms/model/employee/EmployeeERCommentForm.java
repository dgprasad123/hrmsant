package hrms.model.employee;

/**
 *
 * @author lenovo
 */
public class EmployeeERCommentForm {

    private String empId;
    private String profile_comment;
    private String education_comment;
    private String training_comment;
    private String experience_comment;
    private String deputation_comment;

    public String getEmpId() {
        return empId;
    }

    public void setEmpId(String empId) {
        this.empId = empId;
    }

    public String getProfile_comment() {
        return profile_comment;
    }

    public void setProfile_comment(String profile_comment) {
        this.profile_comment = profile_comment;
    }

    public String getEducation_comment() {
        return education_comment;
    }

    public void setEducation_comment(String education_comment) {
        this.education_comment = education_comment;
    }

    public String getTraining_comment() {
        return training_comment;
    }

    public void setTraining_comment(String training_comment) {
        this.training_comment = training_comment;
    }

    public String getExperience_comment() {
        return experience_comment;
    }

    public void setExperience_comment(String experience_comment) {
        this.experience_comment = experience_comment;
    }

    public String getDeputation_comment() {
        return deputation_comment;
    }

    public void setDeputation_comment(String deputation_comment) {
        this.deputation_comment = deputation_comment;
    }

}
