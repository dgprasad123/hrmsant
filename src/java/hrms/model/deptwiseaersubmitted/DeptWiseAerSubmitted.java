
package hrms.model.deptwiseaersubmitted;

public class DeptWiseAerSubmitted {
    private String offName=null;
    private String fiscalYear=null;
    private String status=null;
    private String aerId=null;

    public String getAerId() {
        return aerId;
    }

    public void setAerId(String aerId) {
        this.aerId = aerId;
    }

    public String getOffName() {
        return offName;
    }

    public void setOffName(String offName) {
        this.offName = offName;
    }

    public String getFiscalYear() {
        return fiscalYear;
    }

    public void setFiscalYear(String fiscalYear) {
        this.fiscalYear = fiscalYear;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }
    
    
}
