public class EmployeeSpecialCategoryReport {

    private String empname;
    private String hrmsid;
    private String dateofspecialcategory;
    private String specialcategorysubtype;
    private String officename;
    private String departmentname;
    private String txtperiodFrom;
    private String txtperiodTo;
    private String username;

    public String getEmpname() {
        return empname;
    }

    public void setEmpname(String empname) {
        this.empname = empname;
    }

    public String getHrmsid() {
        return hrmsid;
    }

    public void setHrmsid(String hrmsid) {
        this.hrmsid = hrmsid;
    }

    public String getDateofspecialcategory() {
        return dateofspecialcategory;
    }

    public void setDateofspecialcategory(String dateofspecialcategory) {
        this.dateofspecialcategory = dateofspecialcategory;
    }

    public String getSpecialcategorysubtype() {
        return specialcategorysubtype;
    }

    public void setSpecialcategorysubtype(String specialcategorysubtype) {
        this.specialcategorysubtype = specialcategorysubtype;
    }

    public String getOfficename() {
        return officename;
    }

    public void setOfficename(String officename) {
        this.officename = officename;
    }

    public String getDepartmentname() {
        return departmentname;
    }

    public void setDepartmentname(String departmentname) {
        this.departmentname = departmentname;
    }

    public String getTxtperiodFrom() {
        return txtperiodFrom;
    }

    public void setTxtperiodFrom(String txtperiodFrom) {
        this.txtperiodFrom = txtperiodFrom;
    }

    public String getTxtperiodTo() {
        return txtperiodTo;
    }

    public void setTxtperiodTo(String txtperiodTo) {
        this.txtperiodTo = txtperiodTo;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

}
