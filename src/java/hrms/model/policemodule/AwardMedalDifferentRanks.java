package hrms.model.policemodule;

public class AwardMedalDifferentRanks {
    
    private String rankid;
    private String rankname;
    private String rankdate;
    private String rankyear;
    private String rankmonth;
    private String rankdays;

    public String getRankid() {
        return rankid;
    }

    public void setRankid(String rankid) {
        this.rankid = rankid;
    }

    public String getRankname() {
        return rankname;
    }

    public void setRankname(String rankname) {
        this.rankname = rankname;
    }

    public String getRankdate() {
        return rankdate;
    }

    public void setRankdate(String rankdate) {
        this.rankdate = rankdate;
    }

    public String getRankyear() {
        return rankyear;
    }

    public void setRankyear(String rankyear) {
        this.rankyear = rankyear;
    }

    public String getRankmonth() {
        return rankmonth;
    }

    public void setRankmonth(String rankmonth) {
        this.rankmonth = rankmonth;
    }

    public String getRankdays() {
        return rankdays;
    }

    public void setRankdays(String rankdays) {
        this.rankdays = rankdays;
    }
    
}
